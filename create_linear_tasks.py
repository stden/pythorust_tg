def main():
    try:
        client = LinearClient()
    except LinearError as e:
        print(f"Ошибка инициализации Linear: {e}")
        sys.exit(1)

    created = 0
    errors = 0

    for category, tasks in TASKS.items():
        print(f"\n📁 Категория: {category}")
        print("-" * 40)

        for title, description, priority in tasks:
            try:
                issue = client.create_issue(
                    team_key=TEAM_KEY,
                    title=title,
                    description=f"{description}\n\nКатегория: {category}",
                    priority=priority,
                )
                print(f"  ✅ {issue['identifier']}: {title}")
                print(f"     URL: {issue['url']}")
                created += 1
            except LinearError as e:
                print(f"  ❌ {title}: {e}")
                errors += 1

    print(f"\n{'=' * 50}")
    print(f"📊 Результат: создано {created} задач, ошибок {errors}")


if __name__ == "__main__":
    main()
